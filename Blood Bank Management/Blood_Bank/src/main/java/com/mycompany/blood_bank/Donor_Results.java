    public Donor_Results(int count, String d_bg, String do_city, int d_amount) {
        initComponents();
        d_count = count;
        d_blood_group = d_bg;
        d_city = do_city;
        d_required_amount = d_amount;
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/blood_bank_management","root","Dhanush1Vedant0");
            
            PreparedStatement res_stmt=con.prepareStatement("call get_donor(?, ?, ?)");  
            res_stmt.setString(1, d_blood_group); // var blood group needed
            res_stmt.setString(2, d_city); // var city of recepient
            res_stmt.setInt(3, d_required_amount); // var required amount of blood

            ResultSet rs=res_stmt.executeQuery();
            if (!rs.isBeforeFirst() ) {    
            new Blood_Bank_HomePage().setVisible(true);
            dispose();
            }else{
             while(rs.next()){
             String id = String.valueOf(rs.getInt("donerID"));
             String name = rs.getString("D_name");
             String city = rs.getString("city");
             String blood_group = rs.getString("blood_grp");
             String amount = String.valueOf(rs.getInt("amount"));
             
             String tbData[] = {id, name, city, blood_group, amount};
             DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
             tblModel.addRow(tbData);
             }        
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

